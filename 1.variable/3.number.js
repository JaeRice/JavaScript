// Data Type: single data, primitive

let integer = 123;
let negative = -123;
let double = 1.23;
console.log(integer);
console.log(negative);
console.log(double);

let binary = 0b1111011; // binary number
let octal = 0o173; // 8
let hex = 0x7b; // 16
console.log(binary);
console.log(octal);
console.log(hex);

// Cool tip!
console.log(0 / 123); // 0
console.log(123 / 0); // Infinity
console.log(123 / -0); // -Infinity
console.log(123 / 'text'); // NaN (Not a Number)

