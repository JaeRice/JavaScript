// Data Type: single data, primitive
// string type
let string = 'hi';
string = '"hi"';
console.log(string);

// print special char
string = 'Hi!\nI am Jae\t\tmy name is Jae\\\u09AC';
console.log(string);

// Template Literal ``
let id = 'Elly';
let greetings = "'Hi!, " + id + "\nHave a good day!'";
console.log(greetings);

greetings = `!Hi, ${id}
Have a good day!'`
console.log(greetings);

